from google.colab import files

uploaded = files.upload()

import cv2
from google.colab.patches import cv2_imshow

video = list(uploaded.keys())[0]

cap = cv2.VideoCapture(video)

print("Slow Motion")

while True:
    ret, frame = cap.read()
    if not ret:
        break

    cv2_imshow(frame)

    if cv2.waitKey(200) & 0xFF == ord('q'):
        break

cap.release()
cv2.destroyAllWindows()